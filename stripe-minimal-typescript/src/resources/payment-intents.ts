/**
 * Reviews can be used to supplement automated fraud detection with human
 * expertise.
 *
 * Learn more about [Radar](/radar) and reviewing payments
 * [here](https://docs.stripe.com/radar/reviews).
 */
export interface Review {
  /**
   * Unique identifier for the object.
   */
  id: string;

  /**
   * Time at which the object was created. Measured in seconds since the Unix epoch.
   */
  created: number;

  /**
   * Has the value `true` if the object exists in live mode or the value `false` if
   * the object exists in test mode.
   */
  livemode: boolean;

  /**
   * String representing the object's type. Objects of the same type share the same
   * value.
   */
  object: 'review';

  /**
   * If `true`, the review needs action.
   */
  open: boolean;

  /**
   * The reason the review was opened. One of `rule` or `manual`.
   */
  opened_reason: 'manual' | 'rule';

  /**
   * The reason the review is currently open or closed. One of `rule`, `manual`,
   * `approved`, `refunded`, `refunded_as_fraud`, `disputed`, `redacted`, `canceled`,
   * `payment_never_settled`, or `acknowledged`.
   */
  reason: string;

  /**
   * The ZIP or postal code of the card used, if applicable.
   */
  billing_zip?: string | null;

  /**
   * The charge associated with this review.
   */
  charge?: string | DisputesAPI.Charge | null;

  /**
   * The reason the review was closed, or null if it has not yet been closed. One of
   * `approved`, `refunded`, `refunded_as_fraud`, `disputed`, `redacted`, `canceled`,
   * `payment_never_settled`, or `acknowledged`.
   */
  closed_reason?:
    | 'acknowledged'
    | 'approved'
    | 'canceled'
    | 'disputed'
    | 'payment_never_settled'
    | 'redacted'
    | 'refunded'
    | 'refunded_as_fraud'
    | null;

  /**
   * The IP address where the payment originated.
   */
  ip_address?: string | null;

  ip_address_location?: Review.IPAddressLocation | null;

  /**
   * The PaymentIntent ID associated with this review, if one exists.
   */
  payment_intent?: string | PaymentIntent;

  session?: Review.Session | null;
}

